// Get models by brand
export const getModelsByBrand = (brandName) => {
  return printerModels.filter(model => model.brand.toLowerCase() === brandName.toLowerCase());
};

// Get models by series
export const getModelsBySeries = (brandName, seriesName) => {
  return printerModels.filter(
    model => model.brand.toLowerCase() === brandName.toLowerCase() &&
             model.series.toLowerCase() === seriesName.toLowerCase()
  );
};

// Search models
export const searchModels = (query) => {
  const lowerQuery = query.toLowerCase();
  return printerModels.filter(
    model => 
      model.brand.toLowerCase().includes(lowerQuery) ||
      model.model.toLowerCase().includes(lowerQuery) ||
      model.series.toLowerCase().includes(lowerQuery)
  );
};

